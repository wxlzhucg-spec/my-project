def _correct_city_name(address: str) -> str:
    """
    对地址中的城市名进行纠错。
    例如：'潮头市' → '潮州市'（先把'头'替换为'州'）
    """
    original = address
    # 规则1：已知纠错表
    for wrong, correct in _CITY_CORRECTIONS.items():
        if wrong in address:
            address = address.replace(wrong, correct)
            logger.info("城市名纠错: '%s' → '%s'", wrong, correct)

    # 规则2：常见同音/形近字纠错（针对城市名中的错别字）
    # 例如：'潮头市' 的 '头' → '州'（潮州市）
    char_replacements = {
        "头": "州", "川": "州", "州": "川",
        "太": "太原", "原": "原",
        "鸟": "鸟",  # 防呆
    }
    # 如果纠错后仍是未知地名，尝试修复城市名末字
    if address != original:
        return address

    # 规则3：检测是否是 "X头市" 格式 → 大概率是 "X州市"
    for old, new in [("头市", "州市"), ("川市", "州市"), ("训市", "圳市")]:
        if old in address:
            address = address.replace(old, new)
            logger.info("城市名格式纠错: '%s' → '%s'", old, new)
            break

    return address



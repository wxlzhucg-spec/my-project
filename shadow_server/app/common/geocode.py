def _parse_address(address: str) -> dict:
    """
    使用 cpca 解析中文地址，返回标准化 省/市/区。
    
    >>> _parse_address("甘肃省天水市武山县")
    {'province': '甘肃省', 'city': '天水市', 'area': '武山县'}
    >>> _parse_address("上海浦东")
    {'province': '上海市', 'city': '上海市', 'area': '浦东新区'}
    """
    result = cpca.transform([address])
    if result is None or result.empty:
        return {"province": "", "city": "", "area": ""}

    row = result.iloc[0]
    province = str(row.get("省", "")).strip()
    city = str(row.get("市", "")).strip()
    area = str(row.get("区", "")).strip()

    # cpca 对无法识别的字段返回空字符串或 "None"
    if province in ("", "None", "nan"):
        province = ""
    if city in ("", "None", "nan"):
        city = ""
    if area in ("", "None", "nan"):
        area = ""

    return {"province": province, "city": city, "area": area}


def _lookup_coords(parsed: dict, raw_address: str = "") -> Optional[tuple[float, float]]:
    """
    根据解析后的 省/市/区 在本地坐标库中查找。
    查找优先级：区精确 > 市精确 > 区名模糊(原文) > 省会
    """
    area = parsed.get("area", "")
    city = parsed.get("city", "")
    province = parsed.get("province", "")

    # 1. 精确匹配区/县
    if area:
        if area in _DISTRICT_COORDS:
            logger.info("地理编码命中区级: %s → %s", area, _DISTRICT_COORDS[area])
            return _DISTRICT_COORDS[area]

    # 2. 匹配市（去掉"市"后缀再试）
    if city:
        city_base = city.rstrip("市")
        for key in [city, city_base]:
            if key in _CITY_COORDS:
                logger.info("地理编码命中市级: %s → %s", key, _CITY_COORDS[key])
                return _CITY_COORDS[key]

    # 3. 区名可能就是市名（如 "深圳市" 被识别为区 "深圳"）
    if area:
        area_base = area.rstrip("市区县")
        for key in [area, area_base]:
            if key in _CITY_COORDS:
                logger.info("地理编码区名命中市级: %s → %s", key, _CITY_COORDS[key])
                return _CITY_COORDS[key]

    # 4. cpca 未识别区名时，尝试在原文中模糊匹配区/县名
    #    如 "浦东" 匹配 "浦东新区"、"海淀" 匹配 "海淀区"、"武山" 匹配 "武山县"
    #    优先于省份兜底，因为区级精度更高
    if raw_address and not area:
        for district_name, coords in _DISTRICT_COORDS.items():
            core = district_name.rstrip("新区")
            if core and core in raw_address:
                logger.info("地理编码命中区名模糊: %s → %s → %s", raw_address, district_name, coords)
                return coords

    # 5. 省份 → 省会
    if province and province in _PROVINCE_CAPITAL:
        capital = _PROVINCE_CAPITAL[province]
        if capital in _CITY_COORDS:
            logger.info("地理编码命中省会: %s → %s → %s", province, capital, _CITY_COORDS[capital])
            return _CITY_COORDS[capital]

    return None


def geocode(address: str) -> tuple[float, float]:
    """
    将中文地址转换为 (纬度, 经度) 元组。

    策略：
      1. cpca 解析 → 本地坐标库匹配
      2. 原始地址字符串直接匹配本地库
      3. geopy (Nominatim) 兜底
      4. 最终兜底：上海坐标
    """
    # ── Step 0: 城市名纠错 ──
    address = _correct_city_name(address)

    # ── Step 1: cpca 解析 + 本地匹配 ──
    parsed = _parse_address(address)
    logger.info("地理编码 cpca 解析: %q → %s", address, parsed)

    coords = _lookup_coords(parsed, address)
    if coords:
        return coords

    # ── Step 2: 原始字符串模糊匹配 ──
    for name, c in _CITY_COORDS.items():
        if name in address:
            logger.info("地理编码命中模糊匹配: %s → %s", name, c)
            return c
    for name, c in _DISTRICT_COORDS.items():
        if name in address:
            logger.info("地理编码命中区级模糊: %s → %s", name, c)
            return c

    # ── Step 3: geopy 兜底 ──
    try:
        from geopy.geocoders import Nominatim
        geolocator = Nominatim(user_agent="shadow-ai", timeout=10)
        location = geolocator.geocode(address)
        if location:
            coords = (location.latitude, location.longitude)
            logger.info("地理编码 geopy 成功: %s → %s", address, coords)
            return coords
    except Exception as exc:
        logger.warning("geopy 地理编码失败: %s", exc)

    # ── Step 4: 最终兜底 ──
    logger.warning("地理编码未命中，使用默认坐标（上海）: %s", address)
    return (31.23, 121.47)


def geocode_with_detail(address: str) -> dict:
    """
    地理编码 + 详细信息返回，供测试面板使用。
    
    返回: {
        "address": 原始地址,
        "parsed": {"province": ..., "city": ..., "area": ...},
        "latitude": ..., "longitude": ...,
        "match_level": "district" | "city" | "province" | "fuzzy" | "geopy" | "default",
        "standard_address": "省/市/区" 标准格式,
    }
    """
    # ── Step 0: 城市名纠错 ──
    original_address = address
    address = _correct_city_name(address)

    parsed = _parse_address(address)
    match_level = "default"

    # 检查匹配级别
    coords = _lookup_coords(parsed, address)
    if coords:
        area = parsed.get("area", "")
        city = parsed.get("city", "")
        if area and area in _DISTRICT_COORDS:
            match_level = "district"
        elif city:
            match_level = "city"
        else:
            match_level = "province"
        # 检查是否通过原文模糊匹配到了区级
        if not area:
            for district_name, dc in _DISTRICT_COORDS.items():
                core = district_name.rstrip("新区")
                if core and core in address and dc == coords:
                    match_level = "district"
                    break
    else:
        # 模糊匹配
        for name in _CITY_COORDS:
            if name in address:
                match_level = "fuzzy"
                coords = _CITY_COORDS[name]
                break
        if not coords:
            for name in _DISTRICT_COORDS:
                if name in address:
                    match_level = "fuzzy"
                    coords = _DISTRICT_COORDS[name]
                    break

    if not coords:
        # geopy
        try:
            from geopy.geocoders import Nominatim
            geolocator = Nominatim(user_agent="shadow-ai", timeout=10)
            location = geolocator.geocode(address)
            if location:
                coords = (location.latitude, location.longitude)
                match_level = "geopy"
        except Exception:
            pass

    if not coords:
        coords = (31.23, 121.47)
        match_level = "default"

    # 构造标准地址
    parts = [parsed.get("province", ""), parsed.get("city", ""), parsed.get("area", "")]
    standard_address = "/".join(p for p in parts if p) or address

    return {
        "address": original_address,
        "parsed": parsed,
        "latitude": coords[0],
        "longitude": coords[1],
        "match_level": match_level,
        "standard_address": standard_address,
    }
